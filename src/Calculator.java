
public class Calculator
{
	double d1;
	double d2;

	public Calculator(double d1, double d2) {
		super();
		this.d1 = d1;
		this.d2 = d2;
	}


	public double getD1() {
		return d1;
	}
	public void setD1(double d1) {
		this.d1 = d1;
	}
	public double getD2() {
		return d2;
	}
	public void setD2(double d2) {
		this.d2 = d2;
	}
	
	public  double calculate()
	{
		return d1;
	}
}
